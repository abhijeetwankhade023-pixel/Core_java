package com.java.datatype;

public class DataType {

	public static void main(String[] args) {
		
	int rollNumber = 45;
	
	System.out.println("integer : rollNumber :" +rollNumber);
	
	double percentage = 78.64;
	
	System.out.println("double : percentage :" +percentage);
	
	long phoneNumber = 4575655523l;
	
	System.out.println("long : phoneNumber :" +phoneNumber);
	
	short age = 45;
	
	System.out.println("short : age :" +age);
	
	float average = 46.75f;
	
	System.out.println("float : average :" +average);
	
	byte a = 23;
	
	System.out.println("byte : a :" +a);
	
	char alpha = 'a';
	
	System.out.println("char: alpha:" +alpha);
	
}
	
}