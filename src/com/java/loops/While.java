package com.java.loops;

public class While {
  public static void main(String[] args) {
	
	  for(int i = 0;i<5;i++) {
		  
		  System.out.println("Hello in for loop" +i);
	  }
	  System.out.println("------------------------------------------");
	  
	  int i = 0;
	  while(i<5) {
		  System.out.println("Hello from while loop" +i);
		  i++;
		  
	  }
}
}
